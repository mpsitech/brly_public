/******************************************************************************
 class QryBrlyLocList::VecVOrd
 ******************************************************************************/

uint QryBrlyLocList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "srf") return SRF;
	else if (s == "ica") return ICA;
	else if (s == "tit") return TIT;
	else if (s == "typ") return TYP;
	else if (s == "reg") return REG;
	else if (s == "eqp") return EQP;

	return(0);
};

string QryBrlyLocList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == SRF) return("srf");
	else if (ix == ICA) return("ica");
	else if (ix == TIT) return("tit");
	else if (ix == TYP) return("typ");
	else if (ix == REG) return("reg");
	else if (ix == EQP) return("eqp");

	return("");
};

/******************************************************************************
 class QryBrlyLocList::StatApp
 ******************************************************************************/

QryBrlyLocList::StatApp::StatApp(
			const uint firstcol
			, const uint jnumFirstdisp
			, const uint ncol
			, const uint ndisp
		) : Block() {
	this->firstcol = firstcol;
	this->jnumFirstdisp = jnumFirstdisp;
	this->ncol = ncol;
	this->ndisp = ndisp;

	mask = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
};

bool QryBrlyLocList::StatApp::readXML(
			xmlXPathContext* docctx
			, string basexpath
			, bool addbasetag
		) {
	clear();

	bool basefound;

	if (addbasetag)
		basefound = checkUclcXPaths(docctx, basexpath, basexpath, "StatAppQryBrlyLocList");
	else
		basefound = checkXPath(docctx, basexpath);

	string itemtag = "StatitemAppQryBrlyLocList";

	if (basefound) {
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "firstcol", firstcol)) add(FIRSTCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "jnumFirstdisp", jnumFirstdisp)) add(JNUMFIRSTDISP);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ncol", ncol)) add(NCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ndisp", ndisp)) add(NDISP);
	};

	return basefound;
};

set<uint> QryBrlyLocList::StatApp::comm(
			const StatApp* comp
		) {
	set<uint> items;

	if (firstcol == comp->firstcol) insert(items, FIRSTCOL);
	if (jnumFirstdisp == comp->jnumFirstdisp) insert(items, JNUMFIRSTDISP);
	if (ncol == comp->ncol) insert(items, NCOL);
	if (ndisp == comp->ndisp) insert(items, NDISP);

	return(items);
};

set<uint> QryBrlyLocList::StatApp::diff(
			const StatApp* comp
		) {
	set<uint> commitems;
	set<uint> diffitems;

	commitems = comm(comp);

	diffitems = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
	for (auto it=commitems.begin();it!=commitems.end();it++) diffitems.erase(*it);

	return(diffitems);
};


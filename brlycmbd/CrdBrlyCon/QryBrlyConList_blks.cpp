/******************************************************************************
 class QryBrlyConList::VecVOrd
 ******************************************************************************/

uint QryBrlyConList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "flt") return FLT;
	else if (s == "cor") return COR;
	else if (s == "sta") return STA;

	return(0);
};

string QryBrlyConList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == FLT) return("flt");
	else if (ix == COR) return("cor");
	else if (ix == STA) return("sta");

	return("");
};

void QryBrlyConList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=3;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


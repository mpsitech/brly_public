void PnlBrlyConConMNEquipment::handleDpchAppDoButViewClick(
			DbsBrly* dbsbrly
			, DpchEngBrly** dpcheng
		) {
	ubigint jrefNew = 0;
	string sref;

	if (statshr.ButViewAvail && statshr.ButViewActive) {
		if (xchg->getIxPreset(VecBrlyVPreset::PREBRLYIXUACFLT, jref)) {
			sref = "CrdBrlyFlt";
			xchg->triggerIxRefSrefIntvalToRefCall(dbsbrly, VecBrlyVCall::CALLBRLYCRDOPEN, jref, 0, 0, sref, [&](){ubigint ref = 0; dbsbrly->loadRefBySQL("SELECT ref FROM TblBrlyMFlight WHERE refBrlyMEquipment = " + to_string(recEqp.ref), ref); return ref;}(), jrefNew);
		};
		if (jrefNew == 0) {
			if (xchg->getIxPreset(VecBrlyVPreset::PREBRLYIXUACLOC, jref)) {
				sref = "CrdBrlyLoc";
				xchg->triggerIxRefSrefIntvalToRefCall(dbsbrly, VecBrlyVCall::CALLBRLYCRDOPEN, jref, 0, 0, sref, [&](){ubigint ref = 0; dbsbrly->loadRefBySQL("SELECT ref FROM TblBrlyMLocation WHERE refBrlyMEquipment = " + to_string(recEqp.ref), ref); return ref;}(), jrefNew);
			};
		};

		if (jrefNew == 0) *dpcheng = new DpchEngBrlyConfirm(false, 0, "");
		else *dpcheng = new DpchEngBrlyConfirm(true, jrefNew, sref);
	};
};


void PnlBrlyConConMNEquipment::handleDpchAppDataStgiacqry(
			DbsBrly* dbsbrly
			, QryBrlyConConMNEquipment::StgIac* _stgiacqry
			, DpchEngBrly** dpcheng
		) {
	set<uint> diffitems;
	set<uint> moditems;

	diffitems = _stgiacqry->diff(&(qry->stgiac));

	BrlyQConConMNEquipment* recSelNew = NULL;

	BrlyLRMConnectionMEquipment* _recConReqp = NULL;
	BrlyMEquipment* _recEqp = NULL;

	muteRefresh = true;

	if (!diffitems.empty()) {
		qry->stgiac = *_stgiacqry;

		if (has(diffitems, QryBrlyConConMNEquipment::StgIac::JNUM)) recSelNew = qry->getRecByJnum(_stgiacqry->jnum);

		if (!has(diffitems, QryBrlyConConMNEquipment::StgIac::JNUM) || (diffitems.size() > 1)) {
			qry->rerun(dbsbrly);
			insert(moditems, {DpchEngData::STATSHRQRY, DpchEngData::RST});
		};

		if (has(diffitems, QryBrlyConConMNEquipment::StgIac::JNUM)) {
			if (!recSelNew) recSelNew = qry->getRecByJnum(_stgiacqry->jnum);

			recConReqp = BrlyLRMConnectionMEquipment();

			recEqp = BrlyMEquipment();
			ixWSubsetEqp = 0;

			if (recSelNew) {
				if (dbsbrly->tblbrlylrmconnectionmequipment->loadRecByRef(recSelNew->ref, &_recConReqp)) {
					recConReqp = *_recConReqp;
					delete _recConReqp;
				};
				if (dbsbrly->tblbrlymequipment->loadRecByRef(recSelNew->mref, &_recEqp)) {
					recEqp = *_recEqp;
					ixWSubsetEqp = dbsbrly->getIxWSubsetByBrlyMEquipment(_recEqp);
					delete _recEqp;
				};
			};

			xchg->addRefPreset(VecBrlyVPreset::PREBRLYREFSEL, jref, ((recSelNew) ? recSelNew->ref : 0));
			qry->refreshJnum();
		};

		refresh(dbsbrly, moditems);
	};

	muteRefresh = false;

	insert(moditems, DpchEngData::STGIACQRY);
	*dpcheng = getNewDpchEng(moditems);
};


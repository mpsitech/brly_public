/******************************************************************************
 class QryBrlyLocList::VecVOrd
 ******************************************************************************/

uint QryBrlyLocList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "srf") return SRF;
	else if (s == "ica") return ICA;
	else if (s == "tit") return TIT;
	else if (s == "typ") return TYP;
	else if (s == "reg") return REG;
	else if (s == "eqp") return EQP;

	return(0);
};

string QryBrlyLocList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == SRF) return("srf");
	else if (ix == ICA) return("ica");
	else if (ix == TIT) return("tit");
	else if (ix == TYP) return("typ");
	else if (ix == REG) return("reg");
	else if (ix == EQP) return("eqp");

	return("");
};

void QryBrlyLocList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=6;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


/******************************************************************************
 class QryBrlySegList::VecVOrd
 ******************************************************************************/

uint QryBrlySegList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "ret") return RET;
	else if (s == "reu") return REU;
	else if (s == "sta") return STA;
	else if (s == "sto") return STO;

	return(0);
};

string QryBrlySegList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == RET) return("ret");
	else if (ix == REU) return("reu");
	else if (ix == STA) return("sta");
	else if (ix == STO) return("sto");

	return("");
};

void QryBrlySegList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=4;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


/******************************************************************************
 class QryBrlyRlyList::VecVOrd
 ******************************************************************************/

uint QryBrlyRlyList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "sta") return STA;
	else if (s == "sto") return STO;
	else if (s == "con") return CON;
	else if (s == "dir") return DIR;

	return(0);
};

string QryBrlyRlyList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == STA) return("sta");
	else if (ix == STO) return("sto");
	else if (ix == CON) return("con");
	else if (ix == DIR) return("dir");

	return("");
};

void QryBrlyRlyList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=4;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

